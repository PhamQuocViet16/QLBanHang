    @Override
    public void delete() {
        int selectedRow = tblSanPham.getSelectedRow();
        if (selectedRow == -1) {
            XDialog.alert("Vui lòng chọn sản phẩm cần xoá trong bảng.");
            return;
        }

        if (XDialog.confirm("Bạn thực sự muốn xoá sản phẩm này?")) {
            try {
                int masp = Integer.parseInt(tblSanPham.getValueAt(selectedRow, 0).toString());
                dao.deleteById(masp);

                // Kiểm tra lại loại sản phẩm đang chọn, rồi load lại sản phẩm theo loại
                int selectedLoaiRow = tblLoaiSanPham.getSelectedRow();
                if (selectedLoaiRow >= 0) {
                    this.fillSanPhamTheoLoai();  // Hiển thị lại danh sách theo loại đang chọn
                } else {
                    fillToTable();  // Nếu không chọn loại nào, load toàn bộ
                }

                this.clear();
                XDialog.alert("Xoá sản phẩm thành công!");
            } catch (Exception e) {
                XDialog.alert("Xoá sản phẩm thất bại.");
            }
        }
    }

