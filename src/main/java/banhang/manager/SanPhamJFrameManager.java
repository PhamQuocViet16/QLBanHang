    @Override
    public void create() {
        try {
            SanPham entity = this.getForm();

            // Kiểm tra rỗng
            if (entity.getTensp().isEmpty() || entity.getDvt().isEmpty() || entity.getNuocsx().isEmpty()) {
                XDialog.alert("Vui lòng nhập đầy đủ thông tin sản phẩm.");
                return;
            }

            // Kiểm tra giá hợp lệ
            if (entity.getGia() <= 0) {
                XDialog.alert("Giá sản phẩm phải lớn hơn 0.");
                return;
            }

            dao.create(entity);
            this.fillSanPhamTheoLoai();
            this.clear();
            XDialog.alert("Thêm sản phẩm thành công!");
        } catch (NumberFormatException e) {
            XDialog.alert("Giá không hợp lệ. Vui lòng nhập số.");
        } catch (Exception e) {
            XDialog.alert("Thêm sản phẩm thất bại. Vui lòng kiểm tra dữ liệu.");
        }
    }

