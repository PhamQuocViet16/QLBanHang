    @Override
    public void search() {
        String keyword = txtSearch.getText().trim();
        DefaultTableModel model = (DefaultTableModel) tblSanPham.getModel();
        model.setRowCount(0); // Clear bảng

        if (keyword.isEmpty()) {
            XDialog.alert("Vui lòng nhập mã,tên hoặc loại để tìm kiếm!");
            this.fillSanPhamTheoLoai();
            return;
        }

        try {
            // Nếu là số → tìm theo MASP
            int id = Integer.parseInt(keyword);
            SanPham sp = dao.findById(id);
            if (sp != null) {
                items = List.of(sp);
            } else {
                items = dao.findByName(keyword);
            }
        } catch (NumberFormatException e) {
            // Nếu không phải số → tìm theo tên
            items = dao.findByName(keyword);
        }

        // Nếu không có kết quả → thử tìm theo mã loại
        if (items == null || items.isEmpty()) {
            int maloai = Integer.parseInt(keyword);
            items = dao.findByMaLoai(maloai);  // GỌI HÀM DAO CHÍNH XÁC Ở ĐÂY
        }

        // Nếu vẫn không có gì → báo không tìm thấy
        if (items == null || items.isEmpty()) {
            XDialog.alert("Không tìm thấy sản phẩm với mã, tên hoặc loại: " + keyword);
            this.fillSanPhamTheoLoai();
            return;
        }

        // Hiển thị kết quả
        for (SanPham item : items) {
            model.addRow(new Object[]{
                item.getMasp(),
                item.getTensp(),
                item.getDvt(),
                item.getNuocsx(),
                String.format("%,.0f VNĐ", item.getGia()), // <-- thêm đơn vị ở đây
                item.getMaloai(),
                false
            });
        }
    }

